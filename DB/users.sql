-- Вставляем данные в таблицу players, выбирая случайные amplua_id из таблицы amplua
INSERT INTO players (amplua_id, height, height_level, game_number, user_id)
SELECT
    a.id as amplua_id,
    FLOOR(RAND() * 50) + 150 as height,
    FLOOR(RAND() * 5) + 1 as height_level,
    FLOOR(RAND() * 100) as game_number,
    u.id
FROM users u
INNER JOIN amplua a ON a.id = FLOOR(RAND() * (SELECT MAX(id) FROM amplua ) + 1)
ORDER BY RAND();
